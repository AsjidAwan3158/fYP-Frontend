interface FilterActionsDropdownProps {
    filterId: string;
    onSettings?: (filterId: string) => void;
    onDuplicate?: (filterId: string) => void;
    onDelete?: (filterId: string) => void;
}

function FilterActionsDropdown({
    filterId,
    onSettings,
    onDuplicate,
    onDelete
}: FilterActionsDropdownProps) {
    const [isOpen, setIsOpen] = useState(false);
    const dropdownRef = useRef<HTMLDivElement>(null);

    // Close dropdown when clicking outside
    useEffect(() => {
        const handleClickOutside = (event: MouseEvent) => {
            if (dropdownRef.current && !dropdownRef.current.contains(event.target as Node)) {
                setIsOpen(false);
            }
        };

        if (isOpen) {
            document.addEventListener('mousedown', handleClickOutside);
        }

        return () => {
            document.removeEventListener('mousedown', handleClickOutside);
        };
    }, [isOpen]);

    const handleToggle = (e: React.MouseEvent) => {
        e.stopPropagation();
        setIsOpen(!isOpen);
    };

    const handleSettings = () => {
        onSettings?.(filterId);
        setIsOpen(false);
    };

    const handleDuplicate = () => {
        onDuplicate?.(filterId);
        setIsOpen(false);
    };

    const handleDelete = () => {
        onDelete?.(filterId);
        setIsOpen(false);
    };

    return (
        <div className="relative" ref={dropdownRef}>
            {/* Trigger Button */}
            <button
                onClick={handleToggle}
                className="p-1 text-gray-400 hover:text-gray-600 transition-colors"
            >
                <MoreVerticalIcon />
            </button>

            {/* Dropdown Menu */}
            {isOpen && (
                <div className="absolute right-0 top-full mt-1 w-44 bg-white rounded-lg shadow-lg border border-gray-200 py-1 z-[999]">
                    {/* Settings */}
                    <button
                        onClick={handleSettings}
                        className="w-full flex items-center gap-3 px-4 py-2.5 text-sm text-gray-700 hover:bg-gray-50 transition-colors"
                    >
                        <SettingsIcon />
                        Settings
                    </button>

                    {/* Duplicate filter */}
                    <button
                        onClick={handleDuplicate}
                        className="w-full flex items-center gap-3 px-4 py-2.5 text-sm text-gray-700 hover:bg-gray-50 transition-colors"
                    >
                        <DuplicateIcon />
                        Duplicate filter
                    </button>

                    {/* Divider */}
                    <div className="my-1 border-t border-gray-200"></div>

                    {/* Delete filter (destructive) */}
                    <button
                        onClick={handleDelete}
                        className="w-full flex items-center gap-3 px-4 py-2.5 text-sm text-red-600 hover:bg-red-50 transition-colors"
                    >
                        <TrashIcon />
                        Delete filter
                    </button>
                </div>
            )}
        </div>
    );
}

export default FilterActionsDropdown
